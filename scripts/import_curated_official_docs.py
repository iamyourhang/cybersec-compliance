def ensure_record(item: Dict[str, Any]) -> Dict[str, Any]:
    existing = ComplianceRepository.find_existing(item["name"], item["country_code"])
    if existing:
        return dict(existing)
    record_id = ComplianceRepository.create(
        {
            "name": item["name"],
            "entry_type": item["entry_type"],
            "mandatory": item["mandatory"],
            "status": "active",
            "country_code": item["country_code"],
            "issuing_body": item.get("issuing_body"),
            "official_url": item.get("official_url"),
            "applicable_products": item.get("applicable_products") or [],
            "scope_description": item.get("scope_description"),
            "remarks": "由 curated official import 创建；真实性以官方工件闭环为准。",
            "data_source": "curated_official_import",
            "confidence_score": 100,
        }
    )
    record = ComplianceRepository.get_by_id(record_id)
    return dict(record) if record else {"id": record_id, **item}


def read_validated_local_artifact(local_file: Path) -> tuple[bytes, str]:
    content = local_file.read_bytes()
    guessed_type = mimetypes.guess_type(local_file.name)[0]
    suffix = local_file.suffix.lower()

    if suffix == ".pdf" or (guessed_type or "").lower() == "application/pdf":
        if not content.lstrip().startswith(b"%PDF-"):
            raise ValueError(f"本地工件 {local_file} 不是有效 PDF")
        return content, "application/pdf"

    content_type = guessed_type or "text/html"
    if "html" in content_type.lower():
        lowered = content[:2048].lower()
        if b"<html" not in lowered and b"<!doctype html" not in lowered:
            raise ValueError(f"本地工件 {local_file} 不是有效 HTML")
    return content, content_type


def process_item(
    item: Dict[str, Any],
    parse_docs: bool,
    generate_spec: bool,
    link_only: bool = False,
) -> Dict[str, Any]:
    record = ensure_record(item)
    if link_only:
        return register_link_only_source(record, item)

    ingest = OfficialSourceIngestService()
    review = AuthenticityReviewService()

    local_file = Path(item["local_file"]) if item.get("local_file") else None
    if local_file and local_file.exists():
        content, content_type = read_validated_local_artifact(local_file)
        ingest_result = ingest.ingest_uploaded_source(
            record=record,
            official_url=item["official_url"],
            file_name=local_file.name,
            content=content,
            content_type=content_type,
            artifact_url=item.get("artifact_url"),
            requested_by="system:curated_import",
        )
    else:
        ingest_result = ingest.ingest_manual_source(
            record=record,
            official_url=item["official_url"],
            artifact_url=item.get("artifact_url"),
            requested_by="system:curated_import",
        )

    # 真实性只由“官方来源 + 官方工件闭环”决定；解析、RAG、规格是派生能力。
    # 扫描件或弱文本 PDF 也必须先进入证据库，不能因为规格抽取失败而丢失官方来源。
    review_result = review.register_manual_source(
        record=record,
        ingest_result=ingest_result,
        official_url=item["official_url"],
        evidence_note=item["evidence_note"],
        checked_by="system:curated_import",
    )
    parse_result = None
    index_result = None
    spec_result = None
    parse_error = None
    index_error = None
    if parse_docs:
        try:
            parse_result = DocumentParseService().parse_document(ingest_result["doc_id"], write_to_knowledge=True)
        except Exception as exc:
            parse_error = str(exc)
            DocRepository.set_failed(ingest_result["doc_id"], parse_error)
            parse_result = {"success": False, "error": parse_error}
        if parse_result.get("success"):
            try:
                index_result = DocumentIndexService().index_document(ingest_result["doc_id"])
            except Exception as exc:
                index_error = str(exc)
                DocRepository.set_index_failed(ingest_result["doc_id"], index_error)
                index_result = {"success": False, "error": index_error, "chunk_count": 0}
        else:
            parse_error = parse_result.get("error") or "parse failed"
            index_error = "parse failed; index skipped"
            DocRepository.set_index_failed(ingest_result["doc_id"], index_error)
            index_result = {"success": False, "error": index_error, "chunk_count": 0}
    if generate_spec and item.get("generate_spec") and index_result and index_result.get("success"):
        spec_result = SpecGeneratorService().generate_from_doc(ingest_result["doc_id"])

    return {
        "name": item["name"],
        "country_code": item["country_code"],
        "record_id": str(record["id"]),
        "doc_id": ingest_result["doc_id"],
        "cos_url": ingest_result["cos_url"],
        "canonical_requirement_id": review_result["canonical_requirement_id"],
        "review_case_id": review_result["review_case_id"],
        "parse_success": bool(parse_result and parse_result.get("success")),
        "parse_error": parse_error,
        "index_success": bool(index_result and index_result.get("success")),
        "index_error": index_error,
        "chunk_count": index_result.get("chunk_count") if index_result else 0,
        "spec_count": spec_result.get("spec_count") if spec_result else 0,
        "spec_cos_url": spec_result.get("cos_url") if spec_result else None,
    }


def register_link_only_source(record: Dict[str, Any], item: Dict[str, Any]) -> Dict[str, Any]:
    """Verify an official stable URL without blocking on server-side download.

    This is used when the official page/PDF URL has been manually confirmed but
    the server network cannot reliably fetch the artifact. The URL is still
    tracked as source_artifact_url so the record is auditable and can be picked
    up later by the local artifact bridge.
    """
    official_url = item["official_url"]
    artifact_url = item.get("artifact_url") or official_url
    evidence_note = (
        item["evidence_note"]
        + "；2026-05-10 按官方链接优先入库策略处理：官方 URL 已确认，服务器端工件下载/解析后续由本地补源或 artifact-fetch 闭环。"
    )
    compliance_id = str(record["id"])
    SourceArtifactRepository.upsert_for_compliance(
        compliance_id=compliance_id,
        official_url=official_url,
        artifact_url=artifact_url,
        artifact_type="official_url",
        artifact_sha256=None,
        download_status="pending",
        download_error="official link verified; artifact download deferred",
        document_id=None,
        source_record_id=None,
    )
    ComplianceRepository.update(
        compliance_id,
        {
            "verified": True,
            "official_url": official_url,
            "source_artifact_url": artifact_url,
            "source_artifact_type": "official_url",
            "source_download_status": "pending",
            "source_download_error": "official link verified; artifact download deferred",
            "applicable_products": item.get("applicable_products") or [],
            "scope_description": item.get("scope_description"),
        },
        force=True,
    )
    refreshed = ComplianceRepository.get_by_id(compliance_id) or record
    case_id = ReviewCaseRepository.ensure_for_record(dict(refreshed))
    review_result = AuthenticityReviewService().apply_decision(
        case_id=case_id,
        decision={
            "authenticity_status": "verified",
            "risk_score": 0,
            "reasons": ["official_link_verified", "artifact_download_deferred"],
            "evidence_note": evidence_note,
            "source_download_status": "pending",
            "source_download_error": "official link verified; artifact download deferred",
        },
        checked_by="system:curated_link_import",
    )
    return {
        "name": item["name"],
        "country_code": item["country_code"],
        "record_id": compliance_id,
        "doc_id": None,
        "cos_url": None,
        "canonical_requirement_id": str(review_result.get("canonical_requirement_id") or ""),
        "review_case_id": str(review_result.get("id") or case_id),
        "parse_success": False,
        "parse_error": "link-only import; artifact parsing deferred",
        "index_success": False,
        "index_error": "link-only import; artifact indexing deferred",
        "chunk_count": 0,
        "spec_count": 0,
        "spec_cos_url": None,
    }


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--countries", nargs="*", help="只处理这些国家，如 AU CA CN")
    parser.add_argument("--names", nargs="*", help="只处理这些精确名称的条目")
    parser.add_argument("--skip-parse", action="store_true")
    parser.add_argument("--skip-spec", action="store_true")
    parser.add_argument("--link-only", action="store_true", help="只确认官方链接并入库，下载/解析后续处理")
    args = parser.parse_args()

    selected = CURATED_DOCS
    if args.countries:
        wanted = {code.upper() for code in args.countries}
        selected = [item for item in CURATED_DOCS if item["country_code"] in wanted]
    if args.names:
        wanted_names = set(args.names)
        selected = [item for item in selected if item["name"] in wanted_names]

    results: List[Dict[str, Any]] = []
    errors: List[Dict[str, Any]] = []
    for item in selected:
        try:
            results.append(
                process_item(
                    item,
                    parse_docs=not args.skip_parse,
                    generate_spec=not args.skip_spec,
                    link_only=args.link_only,
                )
            )
        except Exception as exc:
            errors.append(
                {
                    "name": item["name"],
                    "country_code": item["country_code"],
                    "error": str(exc),
                }
            )

    print(
        json.dumps(
            {
                "items": results,
                "errors": errors,
                "total": len(results),
                "error_count": len(errors),
            },
            ensure_ascii=False,
            indent=2,
        )
    )


if __name__ == "__main__":
    main()
